package javaCampSecondLesson;

public class Course {

	public Course(int id, String name, String mentor, double unitPrice, String detail) {
		this.id = id;
		this.name = name;
		this.mentor = mentor;
		this.unitPrice = unitPrice;
		this.detail = detail;
	}
	
		int id;
		String name;
		String mentor;
		double unitPrice;
		String detail;
	
}
